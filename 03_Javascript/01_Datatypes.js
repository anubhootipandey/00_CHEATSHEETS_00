/*****************
 
Javascript has two types of Datatypes:

//Primitive Datatypes:-

1.String---> "Hello" etc.
2.Number---> 5,6,3,etc.
3.Symbol--->used as an identifier for object properties.
4.Boolean--->true or false
5.Undefined--->a variable that has not been assigned a value, is undefined.
6.Null--->represents null i.e, no value at all.

//Non-Primitive Datatypes:-

1.Object--->instance through which we can access members
2.Arrays--->group of similar values

*****************/

//-----------------------------Primitive Datatypes-----------------------------//

// 1.String
let text = 'Hello!';

// 2.Number
let number = 42;

// 3.Boolean
let isJavaScriptFun = true;

// 4.Undefined
let something;

// 5.Null
let empty = null;

// 6.Symbol
let symbol = Symbol("unique");

//---------------------------Non-Primitive Datatypes-----------------------------//

// 1. Object
let person = { name: 'Alice', age: 30 };

// 2.Array
let colors = ['red', 'green', 'blue'];

